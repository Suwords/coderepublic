package cn.coderepublic.variable;

public class TestVar01 {
    public static void main(String[] args) {
        //变量的声明（定义变量）
        //java是一个强类型的语言，只要声明变量就必须定义类型：定义整数类型
        int age; //定义一个整数类型的变量，变量名字为age
        //对变量进行赋值操作：
        age = 10;
        age = 12;
        age = 20;
        age = age + 4;
        age = 9;
        System.out.println(age);
    }
}
